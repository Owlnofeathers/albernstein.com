            <h2>Paranoid Vampires</h2>
              <p>Are visionaries who search hard for the Truth. They see things that others can't. The question is whether those things are really there.</p>
              <ul>
                <li>Look for: absolute certainty about themselves and their beliefs, distrust of everything else.</li>
                <li>Draw you in with: virtue, creativity, perceptiveness, protectiveness, and deliverance from evil.</li>
                <li>Drain you by: jealousy, unshakable prejudices, constant cross-examination of every difference of opinion.  Absolute belief is neither good nor evil, but it is a door through which either may enter.</li>
                <li>The ones you see every day: jealous spouses, conspiracy theorists, and people who post weird things on the internet.</li>
                <li>Defensive strategy: know how to recognize a crazy idea when you hear one. Don't answer questions when they're asked a second time.</li>
              </ul>
