            <h2>Anti-social Vampires</h2>
            <p>Are called that not because they don't like parties, but because they are heedless of normal social rules.  
            They love parties, and any other source of excitement and fun.  They hate boredom worse than a stake through the heart.  
            </p>
            <ul class="list-unstyled">
              <li><strong>Daredevils</strong> - are attracted to thrills like lemmings to cliffs.
                <ul>
                  <li>Look for: sex, drugs, and rock ‘n’ roll.</li>
                  <li>Draw you in with: fun, excitement, and adorable adolescent rebelliousness.</li>
                  <li>Drain you by: overdoing everything exciting, and underdoing everything else.</li>
                  <li>The ones you see every day: cowboys, cowgirls, day traders, party animals, rebels without a cause, and that one lover you just can't seem to forget.</li>
                  <li>Defensive strategy: keep your brain engaged even when theirs are turned off.  Especially then.</li>
                </ul>
              </li>
              <li><strong>Used-car salesmen</strong> - their drug of choice is putting one over on you.
                <ul>
                  <li>Look for: people who swear they're telling the truth. Think about it, who but a liar would expect to be doubted?</li>
                  <li>Draw you in with: instant rapport, smiling sincerity, and the sweet prospect of something for nothing.</li>
                  <li>Drain you by: lying, cheating, stealing, and perhaps getting you to engage in a bit of subterfuge yourself.</li>
                  <li>The ones you see every day: anybody who asks you if you've thought about your financial future.</li>
                  <li>Defensive strategy: always read the fine print, and remember, if a deal sounds too good to be true, it is.</li>
                </ul>
              </li>
              <li><strong>Bullies</strong> are addicted to the raw thrill of seeing you squirm.
                <ul>
                  <li>Look for: anger, threats, and yelling.</li>
                  <li>Draw you in with: the illusion that they are powerful.</li>
                  <li>Drain you by: making you afraid.</li>
                  <li>The ones you see every day: the guy in the pickup who flips you off, and the petty tyrant who runs the finance department.</li>
                  <li>Defensive strategy: remember that the real battle with bullies is not in the dust of the playground, but in your own mind.</li>
                </ul>
              </li>
            </ul>
