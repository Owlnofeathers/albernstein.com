          <h2>How to Protect Yourself</h2>
        </div>
        <ol>
          <li><strong>Know Them, Know Their History, And Know Your Goal</strong>
            <p>The way to anticipate vampires is by knowing how they've acted in the past. Chances are pretty good that they'll do the same thing in the future. 
            The big mistake you can make with vampires is assuming, without evidence, that though their record has been bad in the past, 
            that they have learned their lesson, and will do better this time. When you deal with vampires, always ask yourself what you're trying to accomplish and why. 
            If you're not sure, don't do anything until you've thought about it carefully.</p>
          </li>
          <li><strong>Get Outside Verification</strong>
            <p>Vampires want you to listen to them alone. To control you, they'll try to isolate you from your usual sources of information. 
            Always check out what they say with a trusted friend, especially when you'd rather not. Vampires can't operate in the light of day.</p>
          </li>
          <li><strong>Do What They Don't</strong>
            <p>To prevail over Emotional Vampires you must rush in where they fear to tread. Your greatest strengths lie in doing the things you can do that vampires can't.</p>
          </li>
          <li><strong>Pay Attention To Actions, Not Words</strong>
            <p>What vampires say is often very different from what they do. To avoid being drained, always focus on what they do.</p>
          </li>
          <li><strong>Identify Hypnotic Strategy</strong>
            <p>Vampires are consummate hypnotists. When you see through the smoke and mirrors, their illusions don't work nearly so well.</p>
          </li>
          <li><strong>Pick Your Battles</strong>
            <p>To be an effective vampire fighter, you have to be able to pick the important battles and ignore the rest. You also have to avoid fighting battles you can't possibly win.</p>
          </li>
          <li><strong>Let Contingencies Do The Work</strong>
            <p>A contingency is an if-then situation. If someone does a particular thing, then certain consequences will follow. The only way Emotional Vampires learn anything is by 
            experiencing the consequences of their own behavior. If you're ever tempted to rescue a vampire, think about what you're teaching him or her about how the game of life is played. </p>
          </li>
          <li><strong>Choose Your Words As Carefully As You Pick Your Battles</strong>
            <p>With Emotional Vampires what you say, how you say it, and when you say it are all crucial to the outcomes you are likely to achieve.</p>
          </li>
          <li><strong>Ignore Tantrums</strong>
            <p>When vampires don't get their way, they throw tantrums. They can explode into all sorts of emotional outbursts whose only purpose is to get you to give in. Don't.</p>
          </li>
          <li><strong>Know Your Own Limits</strong>
            <p>Dealing with Emotional Vampires requires a lot of effort. They may be worth it, they may not. Only you can decide. Sometimes it's better to run away, or not get involved in the first place.</p>
          </li>
