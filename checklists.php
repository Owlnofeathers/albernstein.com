            <h2>Obsessive-Compulsive Vampires</h2>
              <p>Are perfectionists who dot every i, cross every t, but can't seem to see the forest for the for the superfluous overabundance of trees.</p>
              <ul>
                <li>Look for: overwork and emotional constipation.</li>
                <li>Draw you in with: competence, hard work, and attention to detail.</li>
                <li>Drain you by: punishing everybody who's not as competent as they are.</li>
                <li>The ones you see every day: puritans, perfectionists, and control freaks.</li>
                <li>Defensive strategy: negotiate to deliver a product, then resist attempts to control the process.</li>
              </ul>
