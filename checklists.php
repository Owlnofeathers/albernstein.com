          <div role="tabpanel" class="tab-pane fade" id="narcissistic">
            <h2>Narcissistic Vampires</h2>
              <p>Have huge egos and tiny consciences. It's not they're actively trying to hurt people; they just never consider other people at all, unless they want something.</p>
              <ul class="list-unstyled">
                <li><strong>Legends in their own minds</strong> with talent like theirs, who needs performance? 
                  <ul>
                    <li>Look for: self-proclaimed genius, with membership in mensa listed as greatest life achievement.</li> 
                    <li>Draw you in with: talent and potential.</li> 
                    <li>Drain you by: blaming everybody else for their inability to realize that potential.</li>
                    <li>The one you see every day: your brother-in-law, the genius who can't hold down a job.</li> 
                    <li>Defensive strategy: getting them to do the parts they don't like first, and paying attention to performance rather than talk.</li>
                  </ul>
                </li>
                <li><strong>Superstars</strong> do what it takes to climb the ladder of success – whatever it takes, especially taking advantage other people.</li>
                  <ul>
                    <li>Look for: success, achieved at all costs.</li> 
                    <li>Draw you in with: talent, charisma, and acting as if you were as cool as they think they are.</li>
                    <li>Drain you by: perpetually seeing their needs as more important than anybody else's, and not giving a rip once they've gotten what they want.</li>
                    <li>The one you're most likely to see every day: politicians, prima-donnas, and hard-driving competitors who throw tantrums when they lose.</li>
                    <li>Defensive strategy: keep a ledger book in your mind.  Make sure superstars pay up front for anything they want from you.  Never accept promises or extend credit.</li>
                  </ul>
                </li>
              </ul>
