            <h2>Histrionic Vampires</h2>
              <p>Histrionic means dramatic.  With these vampires everything’s a show, and what you see is seldom what you get.  
              They crave attention and approval and will do anything to get them, except making good on their promises.</p>
              <ul class="list-unstyled">
                <li><strong>Hams</strong> live to put on a show. If you don't pay enough attention they'll, they'll just shrivel up and die. It will be your fault.
                  <ul>
                    <li>Look for: sexual stereotypes, and highly noticeable looks that can change overnight according to whim, or remain the same for years through the magic of cosmetic surgery. Histrionics have big hair, big muscles, big emotions and not the tiniest clue of who they really are.</li>
                    <li>Draw you in with: charm, beauty, talent, the promise of sex (often unfulfilled), or whatever else it takes to get noticed, except for substance.</li>                 
                    <li>Drain you by: needing more maintenance than a classic car or a rare orchid, but having almost nothing to give back save an occasional great performance.</li>
                    <li>The ones you see every day: celebrity wannabes and drama queens (and kings) whose emotions are a mile wide and an inch deep.</li>
                    <li>Defensive strategy: histrionic lives are soap operas.  Watch, but don't join the cast.</li>
                  </ul>
                </li>
                <li><strong>Passive-aggressives</strong> long for approval so much that they hide any part of themselves that anyone would disapprove of, especially anger and sexuality.
                  <ul>
                    <li>Look for: bright smiles, blatant denial, and more than a passing resemblance to barbie or ken.</li>
                    <li>Draw you in with: being nice, friendly, cheerful, enthusiastic, brave, clean, and reverent.</li>
                    <li>Drain you by: attacks that everyone else sees, but they never acknowledge, or by simply being too forgetful to give you what they promise.  Or maybe they're sick with some vague and undiagnosible illness.</li>
                    <li>The ones you see every day: people who never get angry themselves, but seem to make everybody else angry. “unintentional” sexual harassers, church ladies who cast stones, pure and perfect anorexics, or anyone else who would never think of doing anything unacceptable.</li>
                    <li>Defensive strategy: first and foremost, never try to get passive-aggressives to admit to their own motivation; you will only get a headache.  Remember that they hunger for approval.  Tell them explicitly what it takes to please you and praise them profusely when they do it. The strategy is simple and almost foolproof, but it is seldom employed because it's hard to praise somebody who gives you headaches. Hard as it is, it's far easier than the alternative.</li> 
                  </ul>
                </li>
              </ul>
